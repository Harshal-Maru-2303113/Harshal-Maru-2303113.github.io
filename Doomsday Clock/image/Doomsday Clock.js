
document.getElementById("Show_hideN").innerHTML = "Show more...";
function ShowmoreN() {
    more = document.getElementById("nuclear");
    button = document.getElementById("Show_hideN");
    if (button.innerHTML == "Show more...") {
        more.style.display = "inline";
        button.innerHTML = "Show less...";
    } else {
        more.style.display = "none";
        button.innerHTML = "Show more...";
    }
}

